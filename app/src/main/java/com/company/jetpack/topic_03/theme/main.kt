package com.company.jetpack.topic_03.theme
/**
 * Custom theme
 * Export theme from material3.io -> theme builder
 * Change files  -> ui.theme -> Color.kt , Theme.kt , Type.kt , etc.
 * Important -> Also have to change package location of new files.
 * Only Material 3 components will automatically adopt color changes. Non-Material 3 components
 * will not, so we must explicitly set their colors.
 * How to check this is material3 component or not ->
 *          If the component's package is androidx.compose.material3, it will automatically adopt theme colors
 * link -> https://material-foundation.github.io/material-theme-builder/
 */

